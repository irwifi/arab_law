<p>Haithem Maleh, a former judge and veteran human rights activists who was imprisoned many times in Syria for his work, said that Syria has 15 branches of security and even if the emergency laws are lifted, and all of them will still be immune from prosecution. He added that these security services are above the law. The month long unexpected protest movement against the regime has been demanding for the lifting of these emergency laws. </p>

<p>Because of these protests President Bashar al-Assad moved to lift the emergency laws and was later approved by the Syrian cabinet. At first it was thought that the regime would create a new set of draconian laws to replace the old emergency laws. However, according to a leading Syrian newspaper that is owned by the brother in law of the president; no new anti-terrorism bill is being passed. </p>

<p>An article in Al Watan newspaper, owned by Rami Makhlouf, the most powerful businessman in Syria, noted that the current criminal laws of Syria’s Penal Code are found to be sufficient to fight the threat of terrorism. The paper published that “what the president will issue is not another law to combat terrorism but only to lift the state of emergency.” It further goes to say that Syria laws contain special articles that are sufficient enough to fight terrorism. Ordinary Syrians should be more vigilant as to these laws that are deemed sufficient by the regime. </p>

<p>President Assad in 2008 extended the immunity of all branches of Syria’s security services from prosecution. This presidential decree will remain unaffected even when the emergency laws were lifted. There had been cases wherein air force intelligence arrests 100 people on charges of corruption. Maleh said that the army and air force intelligence should only work on military matters and that they have no authority over the life of civilians. This is an example of how separate security services overlap their roles as domestic policemen under the Syria laws. </p>

