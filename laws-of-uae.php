            <h1>Law of UAE</h1>           
            <h2>The Federal Supreme Council is the main governing body of the seven emirates.</h2>
            <p>The United Arab Emirates is a federation of seven constituent states, thus is governed by a federal government. This emirates federation was formed in 1971, under a provisional constitution. Up until the formation of the constitutional federation, each emirate had it own laws and regulations.</p>

<p>In 1996, two amendments were made to the provisional constitution making it permanent. In addition Abu Dhabi was named the capital city of UAE.</p>

<p>The federal system on government includes a Supreme Council, a Council of Ministers, a parliamentary body, the Federal National Council and independent judiciary. The Federal Supreme Court is the top most body in the judiciary.</p>

<p>The Federal Supreme Council is the main governing body of the seven emirates. It has both legislative and executive powers. The Rulers of the seven emirates form members of the Federal Supreme Council. They elect the president and the vice president from amongst them to rule for a period of five years.</p>

<p>The Federal Supreme Council has legislative and executive powers, which ratifies and decrees <a href="./uae-federal-laws.php" target="_self">federal laws</a>. It plans general policy and approves the nomination of the Prime Minister and if he opts to resign, accepts his resignation or relieves him his post on recommendation of the President.</p>

<p>Like every other country in the world, UAE has various legislations that govern the various sectors in the country. These are federal laws and include immigration laws, employment laws, and company law amongst others.</p>

<h2>UAE Labour Law</h2>

<p>The UAE employment law or the <a href="./uae-labour-laws.php" target="_self">Labor Law</a> is a <a href="./uae-federal-laws.php" target="_self">Federal Law</a>, so it applies to all staff and employees working in the all the emirates of the federation whether nationals or expatriates. The law covers all aspects of employee-employer relationships.</p>

<p>This law is enforced by the Ministry of Labour and Social Affair, while all labour related litigations are adjudicated by the federal and local courts of the UAE.</p>

<p>It governs all aspects of employment contracts, restrictions on employment of juveniles and women, maintenance of records and files, wages, working hours, leave, safety and protection of employees, medical and social care, codes of discipline, termination of employment contracts, end of service benefits, compensation for occupational diseases, labour inspections, penalties, employment related accidents, injuries and fatalities.</p>

<p>The <a href="./uae-labour-laws.php" target="_self">UAE Labour Laws</a> do not however apply to all employees, the exempt ones include, employees of the federal government in the various departments that constitute public service, members of the armed forces, police and security units, domestic servants and agricultural workers.</p>

<p>According to this employment law, an employee is defined as any individual working for a remuneration of any kind in the service of an employer and under his management as well as every hired individual who works in the service of an employer who is subject to the <a href="./uae-laws.php" target="_self">UAE law</a> and so is subject to the provisions of this law.</p>

<p>In the employment contracts, Arabic is recognized as the official language and must be used in all employment records, contracts, files and statements. This includes instructions and memos to employees. Foreign texts may be used but in the event of inconsistency, the Arabic text will prevail.</p>

<p>The duration of employment has to be stipulated in the employment contract, in accordance to the law. The <a href="./uae-labour-laws.php" target="_self">UAE Labour Law</a> defines wages paid to employees as either total or basic wage. This aspect of the law is most relevant while calculating a gratuity for end of service.</p>

<p>An employee who has completed one year of employment or more in continuous service shall be entitled to gratuity at the end of service according to UAE Law on gratuity. </p>

<p>In <a href="./uae-labour-laws.php" target="_self">UAE Labour Law</a> gratuity is calculated on the basic wage other than the total wage. Total wage usually includes benefits paid to an employee and may make up to 40% of the total wage. Gratuity is calculated according to the number of years an employee has worked. In the first five years of employment gratuity is calculated on 21 days wages and on 30 days wage for any additional year. This is based on the last basic wage received by the employee.</p>

<p>Even though foreigner are allowed to work in UAE, the Labour Law states that work is the right of UEA nationals and non nationals must follow the procedures set out by the Ministry of Labour and Social Affairs and in the labour law.</p>

<h2>UAE Companies Commercial Law</h2>

<p>UAE company law has evolved over time with the promulgation and implementation of the Company Law in 1984.</p>

<p>Prior to this law, companies were established according to the local laws and practices of the Municipalities and local authorities in the emirate they were established in. These laws, rules and practices provided that any person or entity intending to conduct business activities I the UAE had to register in the Commercial Register organized by the Competent Authority which was usually the Municipality of the Emirate.</p>

<p>The first prerequisite was to acquire the requisite trade licence issued by the Municipality Department in the particular Emirate where the business was to be carried out, with the exception of business with an exemption from the Ruler of an Emirate or operating by special concession.</p>

<p>It was important for foreign individuals or companies wishing to establish a presence in the UAE to acquire a trade licence issued by the relevant Municipality. Foreign companies could only acquire one licence, while UAE nationals could obtain as many trade licences as their business required.</p>

<p>After the UAE Company law was published in 1984, the Ministry of Economy and Commerce was charged with the task of implementing the law. All companies operating in the UAE were required to conform with the new law. This law classified companies in to 7 types companies with incorporation requirements specified for each type. There was also an inclusion of a special chapter of the law that provides for the registration of foreign companies in the UAE.</p>

<h2>UAE Immigration Law</h2>

<p>Every country has a bunch of laws and regulations to control the flow of foreigners in and out of its bounders. In UAE, the law governing immigration is contained in The Federal Law No.6 of 1973.</p>

<p>The general rule regarding foreign visitors to the UAE is that all visitors require visas apart from those who are no transit – they are passengers who do not leave the airport and citizens of countries that have been exempted by the Government of UAE.</p>

<p>Citizens of such countries that have been exempted do not a visa to enter UAE, but obtain a visitors visa at the port of entrance after paying the prescribed fees. This visitor’s visa is valid for 60 days and an extension of the same can be applied for. </p>

<p>In UAE, the Naturalization and Immigration Administration, the International Airport Authority of any of the member emirates and any other body designated by the Ministry of Interior are the bodies responsible for issuing visas and permits to foreigners wishing to enter UAE.</p>

<p>Entry permits are distinct from visas and these may only be obtained from within UAE, from the Headquarters of immigration and Residence. UAE immigration laws and regulations cover the various interests that visitors may have ranging from business to tourism. It is a good idea to have a basic understanding of these immigration laws, both for business and personal purposes.</p>

<p>There are different types of visas and permits which foreigners are required to have, depending on the nature of their visit. There are also procedures that one is required to follow in order to obtain them. These procedures are governed by the Immigration Law and the regulations and decrees issued by the Ministry of Interior.<br />
  <br />
</p>

