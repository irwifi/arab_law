<p><strong>About the Basic Saudi Laws</strong></p>

<p><b>1.</b> General Principles: This includes Article 1 which declares that God's Book as well as the Sunnah of His Prophet will be the constitution of the country; Riyadh is the capital and Arabic was the official language. </p>

<p><b>2.</b> Monarchy: The rights of Monarch are outlined in Article 7. There is also Article 8 which states that equality, justice and consultation must be as stated by Shari'ah. </p>

<p><b>3.</b> Aspects of the Saudi Family, with Article 9 stating that the members of every family must be brought up on the foundation of the Islamic faith. </p>

<p><b>4.</b> Economic Principles, with Article 18 of Saudi Arabia laws guarding the private property of people. Article 21 includes alms tax. </p>

<p><b>5.</b> Duties and Rights with Article 27 creating a system for social security making the country a welfare state. This has become achievable without high taxes and expropriation because of the significant supplies of oil as well as a population with less than thirty million. </p>

<p><b>6.</b> Authorities of the State with Islam as the building block of governance. According to Article 45, it is stated that religious rulings should be in agreement with Prophet Sunna and the Holy Qur'an. As a result, a group of Islamic clergy as well as research group will be established. In accordance with Article 55, a king must rule based on the Islam rulings and must manage the use of Shari'ah. Next, there is Article 56 which is stating that the king is also the prime minister.  Article 57 outlines clearly that the cabinet as well as other officials with lower ranking should follow Islam. Individuals who deviate can be punished or dismissed. Articles 60 to 62 state that the king is the chief commander who is rendered with powers with regards to war, as well as the country's national security. </p>

<p><b>7.</b> Financial Affairs, with Article 71 specifying that revenue will be entered and then spent based on rules of statutes. According to Article 70, this was to be published on a regular basis within the Official Gazette. </p>

