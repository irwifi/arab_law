            <h2>Arab Laws</h2>
            <p>According to the Human Rights Watch, the two Israeli laws which were implemented recently could affect the Arab residents and would stifle free expression and promote discrimination. It is said that one of the <strong>Arab laws</strong> would authorize the rural communities with Jewish majority to deny Palestinian Arab residents of Israel and various other applicants who are unsuitable for residency, while the other could stop citizens from expressing themselves about important historical moments.</p>

<p>The two laws were passed by Knesset on the 23rd of March, 2011. Officially one of the laws authorizes the committees for admissions in roughly 300 of the communities with Jewish-majority to deny them residency if they cannot satisfy the criteria for social suitability. This measure anchors a practice in law which has been the grounds to reject applications unjustly by Palestinian Arab residents of Israel, in addition to members of the socially marginalized classes like the single parent families and Jews with non-European roots.</p>

<p>The second law could impose heavy fines on any institution that is funded by the government which include cities that provide education and health as well as for expression considered to be negating Israelâ€™s existence as a democratic and Jewish state.</p>

<p>According to the law of the admissions committee, anyone who want to relocate to any area in the Galilee and Negev regions with less than 400 families need to get authorization from the committees which consist of members of the World Zionist Organization or Jewish agency, town residents and others. This law empowers those committees to deny applicants who, amongst other things will be ill-suited to live the way of life of the community or might destroy the fabric of the community.</p>

<p>There are over 300 small communities of this type in Galilee and Negev, which could be the "kibbutzes", smaller cooperative that is sharing property in farming communities known as "moshavs," or perhaps rural towns based on land that is leased from the state. All of these communities have established admissions committees under laws of the Israel Land Authority, which is the state agency that is responsible for leasing the land. However, the committees and procedures for screening were not specifically sanctioned under the national laws.</p>

<p>Even though Palestinian Arabs can be found in the majority throughout Galilee and Negev, the state hasn't allotted lands allowing those Israeli residents to build small communities. Basically, all the communities and towns where the new law applies had been established for and consist of mostly Jewish residents.</p>

<p>Based on the statements from Parliament, there is an indication that the sponsors of Arab laws intended it to enable most of the Jewish communities to keep their existing demographic foundation by excluding the Palestinian Arab people. This is actually an act of discrimination based on national origin, ethnicity and race.</p>
