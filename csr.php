            <h1>Corporate Social Responsibility</h1>           
           <p>Corporate Social Responsibility (CSR) is a corporation's  conscience, citizenship and social performance.  Sustainable responsibility is a form of corporate self-regulation whereby businesses monitor their activities, ensuring they are compliant with the spirit of the law, as well as ethically sound.</p>

<p>Strong Corporate Social Responsibilities can be the deciding factor as to whether a corporation will develop a strong, successful growth or not.</p>

<p>The foundation of CSR is a company's responsibility for their own actions and encourages a positive outcome through activities involving the environment, their consumers, their employees and their communities.  By voluntarily eliminating actions that harm the public, regardless of its legality, leads to a stronger business base that is respected throughout the community.</p>

<p>The term "corporate social responsibility" first came into play in the 1960s when many large corporations were being formed.  There was a need for some structure that would benefit the growth of these corporations and also protect the environment and consumers.</p>

           <h2>Stakeholders</h2>
<p>
Corporate stakeholders are a group of decision makers that affect the actions and the commitments within the corporation. The term "stakeholder" comes from the impact a corporation's activities have on their communities, employees and consumers beyond that of their shareholders.  

This term was first used in 1963 in an internal memorandum at the Stanford Research Institute.  It defined stakeholders as "those groups without whose support the organization would cease to exist."
</p>

<h2>The Purpose of Corporate Social Responsibility</h2>
<p>
Corporate Social Responsibility is an aid and guide to help companies uphold their integrity towards their consumers and their communities.  Business ethics play an extremely powerful role in the development of a company. Ethical activity can either help a company grow or lead to major problems throughout a business and it's eventual downfall.
</p>
<h2>Philanthropical Actions</h2>
<p>
As some large corporations will lean towards philanthropy by making donations and offering aid to  impoverished communities in other countries; others are not quite sold on that approach.  These businesses feel that offering community development in their own backyard leads to a more solid foundation and community friendly model.

Some companies overlook one of the most crucial elements to corporate responsibility; the health and education of their employees.  Employees that are given the opportunity for educational advancement will stay with their company and add to the company's overall growth.  Employees can not contribute to the overall development of a business if they are ill.  Healthcare is essential within the workforce and  will lead to employee loyalty.
</p>
<h2>Creating Shared Value</h2>
<p>Shared value is the concept that a corporation's success and social welfare are one and the same.  For a business to thrive, profitable and competitive business mindsets must be in place.

The wrong approach to CSV is pitting a business against their community by claiming that the costs and limitations brought on by social and environmental standards is harming the business.  This is not to say there are not trade-offs between short-term profitability and social goals, because there are.

Focusing instead on great opportunities by building social values within the corporation will give a stronger, more long-term competitive edge.  Corporate Social Responsibility should be the foundation for any corporation that is looking to the future for growth and sustainability.
</p>
           
                      
                
           
